
class Calculadora:

    def restar(self, numero1, numero2):
        return numero1 - numero2
    
    def sumar(self, numero1, numero2):
        return numero1 + numero2

    def multiplicar(self, numero1, numero2):
        return numero1 * numero2   

    def dividir(self, numero1, numero2):
        return numero1 / numero2



